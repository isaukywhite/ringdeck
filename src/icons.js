// ─── Helper: resolve icon name to SVG markup ───

const DEFAULT_ICON = 'cog-6-tooth';

export function resolveIcon(iconName) {
  if (!iconName) return ICON_MAP[DEFAULT_ICON];
  // Legacy emoji support — if it looks like an emoji, return it as text
  if (/\p{Emoji_Presentation}/u.test(iconName) || iconName.length <= 2) {
    return `<span style="font-size:inherit;line-height:1">${iconName}</span>`;
  }
  return ICON_MAP[iconName] || ICON_MAP[DEFAULT_ICON];
}

export function iconNames() {
  return Object.keys(ICON_MAP);
}
